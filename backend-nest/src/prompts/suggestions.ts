/**
 * Open-Pax — Suggestions Prompt
 * =============================
 * Генератор подсказок/предложений (actions.md)
 */

import { PromptVariables, Suggestion } from './types';
import { parseJsonLoose } from '../utils/json-repair';

/**
 * Построить промпт для генерации подсказок
 */
export function buildSuggestionsPrompt(vars: PromptVariables): string {
  return `Мы создаём пошаговую стратегическую игру. Игрок играет за политию ${vars.PLAYER_POLITY}.

[Твоя роль]

Твоя задача - предложить широкий массив возможных действий для игрока, которые помогут ему в его целях, кампаниях и насущных заботах.

Сначала определи 6-9 "Тем для беспокойства", за которыми игрок должен следить. Это может быть зреющее восстание, которое нужно подавить, экономический вопрос, отношения или конфликт с другими политиями, внутренние дела - всё что угодно, что может волновать игрока. Большинство тем строй на текущем состоянии карты и истории событий игры. Некоторые темы должны касаться конкретных целей игрока - их видно из его прошлых действий.

Затем для каждой темы предложи 2-5 конкретных действий на выбор.

[Формат]

Для каждой "Темы для беспокойства":

Название: Короткое иммерсивное предложение (например, "Предотвращение переворота" или "Растущая угроза на востоке")

Описание: 2-3 предложения, НЕ более 25 слов: суть проблемы, её причины и контекст, почему это важно. Не пиши число слов в тексте.

Для каждого действия:
- Название: иммерсивное название стратегии (например, "Заставить их раскрыться" или "Задушить мятеж в зародыше")
- Содержание: конкретное исполнимое действие (до 30 слов). Привязывайся к карте (называй регионы и политии по именам) и к недавним событиям. Примеры хороших действий: передислокация батальонов в конкретный регион, переговоры с конкретной страной, совместная операция с союзником, распространение дезинформации, демонстрация силы для провокации реакции, тайные приготовления к следующему прыжку. Никаких общих советов вроде "улучшить экономику" - только точные, исполнимые шаги!

[Описание карты]

${vars.GRAND_MAP_DESCRIPTION_NO_CITY}

[Язык]

${vars.LANGUAGE === 'russian' ? 'Отвечай на русском.' : 'Отвечай на английском.'}

[История событий]

${vars.ALL_EVENTS_WITH_CONSOLIDATION || '(Событий пока нет)'}

[Недавняя дипломатия]

${vars.CHATS_NON_CONSOLIDATED_ROUNDS || '(Дипломатии не было)'}

Текущая дата: ${vars.ORIGIN_ROUND_GRAMMATICAL_DATE}

---

Твой вывод ДОЛЖЕН быть в формате JSON:
{
  "suggestions": [
    {
      "topic": "Название темы",
      "description": "Описание проблемы (15-25 слов)",
      "actions": [
        {
          "title": "Название стратегии",
          "content": "Описание действия (до 30 слов)"
        }
      ]
    }
  ]
}

VERY IMPORTANT: Отвечай ТОЛЬКО валидным JSON.`;
}

export function parseSuggestionsResponse(text: string): Suggestion[] {
  try {
    const parsed = parseJsonLoose<any>(text);
    return parsed.suggestions || [];
  } catch (e) {
    console.error('[PARSER] Failed to parse suggestions:', e);
    return [];
  }
}
